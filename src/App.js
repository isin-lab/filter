const Toolbar = ({ filters, activeFilter, setActiveFilter }) => {
  return (
    <div className='container'>
      {filters.map((filter) => (
        <button className='btn'
          key={filter}
          onClick={() => setActiveFilter(filter)}
          style={{
            fontWeight: activeFilter === filter ? "bold" : "normal",
          }}
        >
          {filter}
        </button>
      ))}
    </div>
  );
};

const Portfolio = () => {
  const [activeFilter, setActiveFilter] = useState("All");

  const filteredProjects = activeFilter === "All" ? projects : projects.filter((project) => project.category === activeFilter);

  return (
    <div className='container'>
      <Toolbar filters={["All", "Websites", "Flayers", "Business Cards"]} activeFilter={activeFilter} setActiveFilter={setActiveFilter} />
      <div className='portfolio'>
        {filteredProjects.map((project, index) => (
          <div key={index}>
            <img src={project.img} alt={project.category} />
          </div>
        ))}
      </div>
    </div>
  );
};


const App = () => {
  return (
    <div>
      <Portfolio />
    </div>
  );
};


export default App;
